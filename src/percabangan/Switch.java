package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanjalan=0;
        switch(jalanjalan){
            case 1:
                System.out.println("single");
                break;
            case 2:
                System.out.println("Dated");
                break;
            case 3:
                System.out.println("kacang");
                break;
            case 4:
                System.out.println("dinner");
                break;
                default:
                    System.out.println("tak kasat mata uwu");
                    break;
        }
    }
}
